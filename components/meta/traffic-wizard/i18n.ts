export type TrafficI18n = { [K in keyof typeof translations['tr']]: string }

export function getLocale(): 'tr' | 'en' {
  if (typeof document === 'undefined') return 'tr'
  const cookie = document.cookie.split('; ').find(c => c.startsWith('NEXT_LOCALE='))
  return (cookie?.split('=')[1] as 'tr' | 'en') ?? 'tr'
}

export function getTrafficI18n(): TrafficI18n {
  return translations[getLocale()]
}
