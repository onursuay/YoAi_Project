export type WizardLocale = keyof typeof translations
export type WizardTranslations = { [K in keyof typeof translations['tr']]: string }

export function getLocaleFromCookie(): WizardLocale {
  if (typeof document === 'undefined') return 'tr'
  const match = document.cookie.match(/(?:^|;\s*)NEXT_LOCALE=([^;]*)/)
  const locale = match?.[1]?.trim()
  return locale === 'en' ? 'en' : 'tr'
}

export function getWizardTranslations(locale: WizardLocale): WizardTranslations {
  return translations[locale] ?? translations.tr
}

